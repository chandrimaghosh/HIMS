public class Vendor {
   
            int vendor_id;
            String vendor_name;
            String vendor_address;
            String vendor_uname;
            String vendor_pass;
            String vendor_email;

    public int getVendor_id() {
        return vendor_id;
    }

    public void setVendor_id(int vendor_id) {
        this.vendor_id = vendor_id;
    }

    public String getVendor_name() {
        return vendor_name;
    }

    public void setVendor_name(String vendor_name) {
        this.vendor_name = vendor_name;
    }

    public String getVendor_address() {
        return vendor_address;
    }

    public void setVendor_address(String vendor_address) {
        this.vendor_address = vendor_address;
    }

    public String getVendor_uname() {
        return vendor_uname;
    }

    public void setVendor_uname(String vendor_uname) {
        this.vendor_uname = vendor_uname;
    }

    public String getVendor_pass() {
        return vendor_pass;
    }

    public void setVendor_pass(String vendor_pass) {
        this.vendor_pass = vendor_pass;
    }

    public String getVendor_email() {
        return vendor_email;
    }

    public void setVendor_email(String vendor_email) {
        this.vendor_email = vendor_email;
    }
    
}
